function fixFile(filePath) {
  let content = fs.readFileSync(filePath, 'utf8');
  
  // Remove lucide-vue-next imports
  content = content.replace(/import\s*{\s*[^}]*}\s*from\s*['"]lucide-vue-next['"];?\s*/g, '');
  
  // Replace icon components with SVG
  Object.keys(iconMap).forEach(iconName => {
    const regex = new RegExp(`<${iconName}\\s+class="[^"]*"\\s*/>`, 'g');
    content = content.replace(regex, iconMap[iconName]);
  });
  
  fs.writeFileSync(filePath, content);
}

// Find all Vue files
function findVueFiles(dir) {
  const files = fs.readdirSync(dir);
  files.forEach(file => {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);
    if (stat.isDirectory()) {
      findVueFiles(filePath);
    } else if (file.endsWith('.vue')) {
      fixFile(filePath);
    }
  });
}

